// x++ retorna o valor de x + 1 para o próprio x...

// ++x tem o mesmo efeito porém com uma funcionalidade a mais, citada no exemplo abaixo


// exemplo com x++
let x = 12

let y = x++ // aqui y recebe o valor de x (= 12) e após x recebe x + 1

console.log(x) // 13
console.log(y) // 12


//exemplo com ++x
let x1 = 12

let y1 = ++x1 //aqui x1 recebe x1 + 1 e após y1 recebe o valo de x1 atualizado (= 13)

console.log(x1) //13
console.log(y1) //13


